                        <b>A importância de enxergar a questão de gênero para além do binarismo</b>
 <br>						<br>
						Quando você nasce, te chamam de homem ou de mulher. Mas e se você não se identificar com nenhum desses dois gêneros e seus comportamentos? É desse ponto que surge o gênero não-binário, pertencente à comunidade transexual.
						 <br>						<br>
						NÃO-BINÁRIO: pessoa cujo gênero não se limita ao masculino ou feminino.
						 <br>						<br>
						Antes de tudo, é muito importante relembrar a diferença existente entre sexo e identidade de gênero, pois somente assim você será capaz de entender o não-binarismo. Gênero é uma construção social, ou seja: ser mulher, no imaginário social, é carregar comportamentos específicos e enfrentar estereótipos pré-determinados. É aí que o não-binarismo surge: é quando você não se identifica com nenhuma das expressões binárias de gênero.
						 <br>						<br>
						História dos não-binários: É muito importante frisar que a colonização e o imperialismo apagaram muitas das identidades de gênero existentes ao redor do mundo. Como exemplo, podemos citar os "Mahu" da Polinésia, que possuem uma identidade de gênero ambígua, com características masculinas e femininas; eles existem até hoje e o termo também é usado em referência aos transexuais. Os incas, por sua vez, cultuavam Chuqui Chinchay, uma divindade que incorporava os gêneros masculino e feminino, sem se prender a uma só definição. Como podemos observar, a colonização impôs o binarismo nas nações que sangraram.
						 <br>						<br>
						“Procurei vestígios na história desde a idade antiga de transexuais, pois primeiro este tema veio à tona na modernidade, quando percebi questões de não identificação com a divisão dos gêneros tradicionais, masculino e feminino, desde a mitologia grega, e como a não-binariedade já era vista no Egito. Ela é milenar. Por outro lado, a sociedade sabe que existe a homossexualidade e com ele convive milenarmente, mas a oprime para manter o modelo da família como uma instituição que deu suporte aos interesses, como quando a Igreja exercia poder de Estado.” - Vânia Petermann, juíza catarinense.
						 <br>						<br>
						É preciso respeitar a multiplicidade existente entre os não-binários. Não existe um jeito certo de ser ou um estilo certo para ter. Para ser não-binário, basta romper com as imposições binárias de gênero. A luta tem ganhado força recentemente, mas a existência é milenar.
						Romper com a não binariedade no século 21 é sinônimo de resistência e luta para o reconhecimento de identidades que fogem dos padrões já estabelecidos e ultrapassados. Toda a força, visibilidade e representatividade do mundo à comunidade não-binária!

                        
