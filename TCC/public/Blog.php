						<br>						<br>
						Nós vivemos em um dos países que mais mata pessoas LGBTQIA+ no mundo. Vivemos em meio à repressão e ao ódio dos que, geralmente, estão ao nosso redor. Em meio aos comentários maldosos, piadas, inseguranças e o sentimento de culpa, infelizmente é comum que as ideias ditas como corretas pela cis-heteronormatividade dominem nossa mente.
						<br>						<br>
						Assim, quando percebemos que talvez sejamos parte da comunidade, é muito comum que passemos a sentir ódio de nossos corpos. Reprimimos nossos sentimentos, nossas vontades, nossas vivências e gostos. Nos reprimimos tanto que, em algum momento, explodimos. Na homofobia internalizada, você é seu próprio alvo e se enxergar parece impossível.<br>						<br>
						Quando explodimos, estamos cheios de traumas. A diferença é que alguns conseguem superá-los e começam a sentir orgulho de serem quem são. Todavia, outros continuam perpetuando a homofobia internalizada. O preconceito domina e o jovem luta contra sua própria sexualidade ou identidade de gênero, refletindo isso para outras pessoas LGBTQIA+.<br>						<br>
						Muitas personalidades LGBTQIA+ da contemporaneidade são criticadas pela própria comunidade, pois questionam a aceitação alheia e perpetuam estereótipos negativos. Sendo assim, é de extrema importância reconhecê-los e perceber se não estamos sendo influenciados por eles. É importante, também, reconhecer a sua própria situação: você é vítima da homofobia internalizada?<br>						<br>
						Se sim, saiba que existe cura: Terapia! É a melhor opção. Se fortaleça como jovem LGBTQIA+. Por meio do autoconhecimento e da busca por representatividades que, de fato, se conectem contigo, é somente assim que a homofobia internalizada deixará de te perturbar. Como membros da comunidade, enfrentamos e enfrentaremos muitas dificuldades. Reprimir a si mesmo é somente uma delas.<br>						<br>
						Você não está sozinho. Nós nunca estamos. Mesmo que pareça que você está no fundo do poço, existem outros de nós em situação parecida. Vamos nos reerguer juntos!

                        
